-- NOTE: 快速构建函数
--       前景，背景，gui 设置这样的顺序
local function hi(group, opts)
  local gui = opts.gui and "gui=" .. opts.gui or ""
  vim.cmd(string.format(
    "highlight %s guifg=%s guibg=%s %s",
    group,
    opts.fg or "none",
    opts.bg or "none",
    gui)
  )
end

